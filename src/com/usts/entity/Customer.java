package com.usts.entity;

public class Customer {
	private Integer id; // 编号
	private String khno; // 客户业务账号
	private String name; // 客户名称
	private String area; // 客户所在地区
	private String cusManager; // 客户负责人
	private String level; // 客户等级
	private String xyd; // 客户信用度
	private String address; // 客户地址
	private String postCode; // 邮政编码
	private String phone; // 联系电话
	private String khzh; // 账务账号
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getKhno() {
		return khno;
	}
	public void setKhno(String khno) {
		this.khno = khno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getCusManager() {
		return cusManager;
	}
	public void setCusManager(String cusManager) {
		this.cusManager = cusManager;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public String getXyd() {
		return xyd;
	}
	public void setXyd(String xyd) {
		this.xyd = xyd;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPostCode() {
		return postCode;
	}
	public void setPostCode(String postCode) {
		this.postCode = postCode;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getKhzh() {
		return khzh;
	}
	public void setKhzh(String khzh) {
		this.khzh = khzh;
	}
	
}
